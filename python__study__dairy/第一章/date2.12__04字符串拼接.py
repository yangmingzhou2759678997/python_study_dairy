#一、字符串拼接使用“+”，但只能拼接两个字符串，若不是字符串则必须先转成字符串
s1="我的人生会很精彩"
s2="我用python去实现"
print(s1 + s2)
print("小杨说:" , s1 +','+ s2 )

#案例（拼接符+）
myname="小杨"
age=24
hobby="唱跳篮球"
